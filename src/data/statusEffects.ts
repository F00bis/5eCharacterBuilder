export function createCustomStatusEffect(
  name: string,
  category: 'harmful' | 'neutral' | 'beneficial',
  description: string = 'A custom status effect'
): StatusEffect {
  return {
    id: `custom-${crypto.randomUUID()}`,
    name,
    category,
    description,
    isCustom: true,
    isStacking: false,
  };
}
